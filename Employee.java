public class Employee {
    String name,address;
    int year;
    public Employee(String n,int y,String add){
        name=n;
        year= y;
        address=add;
    }
    public String getName(){
        return name;
    }
    public int getyear(){
        return year;
    }
    public String getaddress(){
        return address;
    }
}


    

    




    
